[CmdletBinding()]
param (
  [parameter(Mandatory = $true)]
  [string[]]$SourceDirs,

  [Parameter(Mandatory = $true)
  ][ValidateNotNullOrEmpty()]
  [string]$outputDir
)

$searchPatterns = @()
$searchPatterns += '*.sarif'
$searchPatterns += 'TEST*.xml'

If (Test-Path $outputDir) {
  Write-Output "Output directory '$outputDir' already exists"
} else {
  Write-Output "Output directory '$outputDir' does nto exists. try to create it now."
  $newDir = New-Item -Path $outputDir -Force -Type Directory
  if (!$newDir) {
    Throw "Unable to create output directory '$outputDir'."
    Exit 1
  }
}

foreach ($searchPattern in $searchPatterns) {
  Foreach ($sourceDir in $sourceDirs) {
    if (!(Test-Path $sourceDir)) {
      Write-warning "Source directory '$sourceDir' does not exist. Skip."
      continue
    } else {
      Write-Output "Searching for '$searchPattern' files in $sourceDir"
      $sarifFiles = Get-ChildItem -path $sourceDir -filter $searchPattern -Recurse
      foreach ($sarifFile in $sarifFiles) {
        Write-Output " - Copying $($sarifFile.FullName) to '$outputDir'"
        Copy-Item -Path $($sarifFile.FullName) -Destination $outputDir
      }
    }
  }
}

Write-Output "Done"
